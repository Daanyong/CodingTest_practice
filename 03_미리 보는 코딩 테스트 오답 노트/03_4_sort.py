# 정렬 기초 다지기

# 1. 오름차순 정렬
A = [5, 3, 2, 4, 1] # 원본 리스트 선언

# 방법 1: list.sort() - 리스트 자체를 정렬 (in-place)
A.sort()
print("sort() 결과: ", A)

A = [5, 3, 2, 4, 1] # 원본 리스트 다시 초기화

# 방법 2: sorted() - 정렬된 복사본을 반환 (원본 유지)
B = sorted(A)
print("원본 리스트: ", A)
print("sorted() 결과: ", B)

# 2. 내림차순 정렬
A = [5, 3, 2, 4, 1] # 원본 리스트 선언

# 방법 1: 리스트 자체를 내림차순 정렬
A.sort(reverse=True) # sort() 내림차순 정렬
print("내림차순 결과: ", A)

A = [5, 3, 2, 4, 1] # 원본 리스트 다시 초기화

# 방법 2: 정렬된 복사본 반환 (원본은 유지)
B = sorted(A, reverse=True) # sorted() 내림차순 정렬
print("내림차순 복사본: ", B)

# 3. 내림차순 정렬 2
A = [5, 3, 2, 4, 1] # 원본 리스트 선언

# 부호를 반전시키고 오름차순으로 정렬한 후, 다시 부호 되돌리기
A = [-x for x in A] # 리스트 컴프리헨션을 사용하여 각 요소의 부호를 반전
A.sort() # 오름차순 정렬
A = [-x for x in A] # 다시 부호를 되돌려 내림차순 결과 얻기

print("부호 반전 방식 내림차순: ", A)